async function handleKtMessage(message, guildData) {
    if (message.author.bot || !message.guild) return;

    if (message.content.trim() === '-كت') {
        const isAdmin = message.member.permissions.has('Administrator');
        const allowedChannels = guildData.ktChannels || (guildData.ktChannel ? [guildData.ktChannel] : []);

        // التحقق من الروم المخصص
        if (allowedChannels.length > 0 && !isAdmin && !allowedChannels.includes(message.channel.id)) {
            return message.reply({ 
                content: `❌ ممنوع استخدام أمر الكت هنا! يرجى استخدامه في الرومات المخصصة فقط.`, 
                allowedMentions: { repliedUser: true } 
            }).then(msg => {
                setTimeout(() => msg.delete().catch(e => {}), 5000);
            });
        }

        const randomQuestion = ktQuestions[Math.floor(Math.random() * ktQuestions.length)];

        const ktEmbed = new EmbedBuilder()
            .setTitle('❓ كت | سؤال تفاعلي')
            .setDescription(`**${randomQuestion}**`)
            .setColor(0x9b59b6)
            .setFooter({ text: `بواسطة: ${message.author.username}`, iconURL: message.author.displayAvatarURL() })
            .setTimestamp();

        // الرد المباشر (Reply) على رسالة العضو مع بقاء رسالته الأصلية دون حذف
        return message.reply({ 
            embeds: [ktEmbed], 
            allowedMentions: { repliedUser: true } 
        });
    }
}

module.exports = { handleKtMessage };